def to_title_case(input_string):
    # Convert the input string to title case using str.title()
    title_cased_string = input_string.title()
    return title_cased_string

# Input string
input_string = "this is an example string to be converted to title case"

# Convert to title case
title_cased_string = to_title_case(input_string)

# Print the result
print(title_cased_string)
